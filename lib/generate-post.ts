export async function generateAccountPost(profile: Profile) {
  return (await generateAccountPostDetails(profile)).text;
}

export async function generateAccountPostDetails(profile: Profile): Promise<GeneratedPost> {
  const hour = tokyoHour();
  const period = hour < 2 || hour >= 23 ? "late" : hour < 10 ? "morning" : hour < 14 ? "midday" : hour < 18 ? "afternoon" : "evening";
  const library = profile.accountKey === "ai_gal_mama" ? aiGal : profile.accountKey === "ouchiwork_mari" ? ouchiwork : null;

  if (!library) return genericPost(profile);
  const group = library[period];
  const seed = Date.now() + Math.floor(Math.random() * 1_000_000);
  const text = [pick(group.hooks, seed), pick(group.bodies, seed >> 3), pick(group.endings, seed >> 6)].join("\n\n");
  return { text: applyRules(text, profile.rules), category: group.category };
}

function genericPost(profile: Profile): GeneratedPost {
  const theme = clean(profile.theme) || "毎日を少しよくする工夫";
  const audience = clean(profile.audience) || "毎日をがんばっている方";
  const options = [
    `「${theme}、何から始めたらいい？」\n\nそんな${audience}へ。まずは今日できることを、ひとつだけ選んでみよう。\n\nあなたは何から始めますか？`,
    `${theme}は、全部を一気に変えなくて大丈夫。\n\n今日ひとつ試して、合わなければ調整する。それだけでも前進です。`,
    `今日の小さな問い。\n\n${theme}について、今の自分が無理なく続けられることは何だろう？`,
  ];
  return { text: applyRules(pick(options, Date.now()), profile.rules), category: "テーマ投稿" };
}

function applyRules(value: string, rules: string) {
  let text = value;
  if (/絵文字なし|絵文字は使わない/.test(rules)) text = text.replace(/[\p{Extended_Pictographic}\uFE0F]/gu, "");
  if (/質問なし|問いかけなし/.test(rules)) text = text.replace(/\n\n[^\n]*(?:？|\?)[^\n]*$/u, "");
  return text.replace(/\n{3,}/g, "\n\n").trim().slice(0, 500);
}

function pick<T>(values: T[], seed: number) {
  return values[Math.abs(seed) % values.length];
}

function clean(value: string) {
  return value.replace(/\s+/g, " ").trim().slice(0, 80);
}
